package com.trailblazers.freewheelers.model;

import java.math.BigDecimal;

public class InvoiceItem {
    private BigDecimal unitPrice;
    private BigDecimal net;
    private Long quantity;
    private double vatRate;
    private BigDecimal vat;
    private BigDecimal gross;
    private String name;

    public Long getQuantity() {
        return quantity;
    }

    public InvoiceItem setQuantity(Long quantity) {
        this.quantity = quantity;
        return this;
    }

    public BigDecimal getNet() {
        return net;
    }

    public InvoiceItem setNet(BigDecimal net) {
        this.net = net;
        return this;
    }

    public double getVatRate() {
        return vatRate;
    }

    public InvoiceItem setVatRate(double vatRate) {
        this.vatRate = vatRate;
        return this;
    }

    public BigDecimal getVat() {
        return vat;
    }

    public InvoiceItem setVat(BigDecimal vat) {
        this.vat = vat;
        return this;
    }

    public BigDecimal getGross() {
        return gross;
    }

    public InvoiceItem setGross(BigDecimal gross) {
        this.gross = gross;
        return this;
    }

    public String getName() {
        return name;
    }

    public InvoiceItem setName(String name) {
        this.name = name;
        return this;
    }

    public BigDecimal getUnitPrice() {
        return unitPrice;
    }

    public InvoiceItem setUnitPrice(BigDecimal unitPrice) {
        this.unitPrice = unitPrice;
        return this;
    }
}
